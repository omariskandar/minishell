#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned	int, char))
{
	size_t	i;
	size_t	size;
	char	*res;

	if (!s || !f)
		return (NULL);
	size = ft_strlen(s);
	res = (char *)malloc(sizeof(char) * (size + 1));
	if (!res)
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
